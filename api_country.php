/**
 * Get country code from name
 *
 * @param string $name
 * @return string
 */
function api_country_get_code($name) {
	return array_search(
		strtoupper(trim($name)),
		array_map('strtoupper', api_country_all())
	);
}

/**
 * Get country name from code
 *
 * @param string $code
 * @return string
 */
function api_country_get_name($code) {
	$code = strtoupper(trim($code));
	$all = api_country_all();

	return isset($all[$code]) ? $all[$code] : false;
}

